                    <br>
                    <p>Al hablar del bienestar de niños, niñas y adolescentes uno de los principales focos de atención es la forma en la que sus padres se comenzarán a relacionar con ellos (as) desde sus primeros años de vida hasta su adolescencia, esto será de suma <b>importancia para un mejor y adecuado desarrollo tanto físico, social y emocional, siendo la base para la </b>. Es importante saber que ningún padre, madre o cuidador es perfecto, cada uno (a) hará lo mejor que considere desde el amor y el cuidado y usará las herramientas y recursos con los que cuente para lograr un buen vínculo con sus hijos e hijas, sin embargo, siempre es relevante poder conocer cuáles son estas <b>habilidades o competencias</b> con las que contamos para ayudar a nuestros hijos (as) en cada <b>etapa de su crecimiento y frente a las dificultades que se presentan en la vida cotidiana</b>, ya que afectarán positiva o negativamente el desarrollo de niños y niñas.</p>
                    <p>Las habilidades parentales o competencias parentales son conocidas como los <b>conocimientos y capacidades</b> con las que cuentan padres, madres y cuidadores para poder guiar su <b>comportamiento parental de manera práctica</b>, en las distintas situaciones que se dan en la vida familiar, la crianza y en los diversos ámbitos del desarrollo del niño o niña. Permitiendo <b>potenciar su bienestar y garantizar sus derechos</b>, esto será conocido como <b>Parentalidad Positiva</b>, la que se ve reflejada en: el vínculo, desarrollo socio-emocional, cómo se organiza la vida cotidiana, formas de comunicación, satisfacción de necesidades básicas, búsqueda de apoyo, entre otras.</p>
                    <p>Los principales beneficios de conocer y fortalecer las competencias parentales hacia las relaciones basadas en el buen trato con niños, niñas y adolescentes se verán reflejados en las diversas dimensiones de su desarrollo, así podrá por ejemplo: mejorar su autoestima, habilidades sociales, capacidad de resolver problemas, habilidades cognitivas, mejor rendimiento académico, regular sus emociones y enfrentar de mejor manera situaciones que le generen estrés o ansiedad, entre otros.</p>
                    <p>Por otro lado, es esencial identificar y conocer las habilidades parentales de cada padre, madre o cuidador especialmente frente a <b>situaciones complejas de la vida cotidiana</b>, como por ejemplo identificar cuando es o no una “pataleta” o “rabieta” y saber cómo manejarlas, ya que estas <b>se presentan de forma normal</b> en el desarrollo de niños y niñas entre los 2 y 4 años de edad. Es importante conocer que estas conductas son <b>reacciones emocionales intensas que dan cuenta la mayoría de las veces de frustración</b> y estas van desapareciendo luego de los 4-5 años, siendo parte de la vida cotidiana de muchas familias, es así que ante esto lo principal es reconocer que estrategias estamos utilizando y desarrollarlas en base al <b>respeto y la contención de las emociones</b> de nuestros hijos e hijas que aún no tienen las herramientas necesarias para expresarlas.</p>
                    <p>Si quieres conocer más estrategias de manejo de conductas o situaciones difíciles en la vida cotidiana de nuestros hijos e hijas y compartir tu experiencia con otros padres y madres, no te quedes fuera del taller de Habilidades Parentales a desarrollar en nuestro centro.</p>
