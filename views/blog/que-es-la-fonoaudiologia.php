                        <p>Es la disciplina encargada del estudio y tratamiento de la <b>comunicación humana</b>, en procesos normales y/o desordenados, tratando de prevenir dificultades en las áreas del habla, lenguaje, audición, deglución y voz.</p>
                        <p>Un fonoaudiólogo puede intervenir en todas las etapas de desarrollo del sujeto, es decir, desde lactantes hasta adultos mayores. <b>estimulando y/o ayudando para que este logre la comunicación verbal y no verbal</b>. La intervención se aborda de diversas maneras dependiendo las diferentes capacidades y edad del paciente.</p>
