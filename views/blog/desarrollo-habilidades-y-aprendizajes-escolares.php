                    <br>
                    <p>Muchos padres buscan como apoyar a sus hijos en el ámbito escolar, pero no siempre encuentran las herramientas para lograrlo.</p>
                    <p>El primero paso que se debe tener en cuenta es que los niños deben contar con una serie de <b>habilidades o procesos</b> para poder lograr un buen desempeño académico. Si existe alguna dificultad es necesaria la <b>evaluación, diagnostico e intervención</b> de un especialista en el área, en este caso el <b>psicopedagogo</b> que hoy podemos encontrar tanto dentro como fuera del colegio, pero también es fundamental el apoyo y orientación de la familia para que sepan como facilitar herramientas en el hogar.</p>
                    <p>Para poder prestar la ayuda que tu hijo requiere es necesario que la familia se <b>eduque</b> en varios aspectos, pero el menos común pero más importantes, es conocer los <b>procesos cognitivos</b> (percepción, atención, memoria, lenguaje, etc.) y las <b>funciones ejecutivas</b> (organización, planificación, flexibilidad, etc.). Los conceptos mencionados son un conjunto de destrezas o habilidades que precisan ser desarrolladas en el cerebro con las cuales se permitirá adquirir nuevos conocimientos a través de captar, codificar, almacenar, utilizar la información y estos serán la base de nuevos aprendizajes.</p>
                    <p>Para comprender de manera más sencilla se contextualizará con un ejemplo; ¿Qué sucede si tú hijo presenta dificultades en la ejecución de su escritura? en la letra (legible o ilegible) y en sus movimientos (de pequeña o gran progresión) pero no hay precedentes de problemas motores. Crees que ¿Solo se debe insistir con la conocida caligrafía? lo más probable es que creas que sí, pero en realidad es aquí donde entran en juego las bases de los aprendizajes (procesos cognitivos), dedicando horas y horas a la caligrafía no lograras que adquiera el aprendizaje que buscas, pero si se trabaja la <b>coordinación viso-motriz, la orientación especial y la lateralidad</b>, mediante otras estrategias se lograra disminuir, anular o encontrar una alternativa a la dificultad que tu hijo presenta. Por esto es necesario saber qué proceso cognitivo está vinculado a cada aprendizaje, ya que en el caso de existir alguna necesidad educativa permanente o transitoria es necesario <b>intervenir</b> desde la base para lograr potenciar y desarrollar un aprendizaje.</p>
                    <p>Todos pueden presentar alguna necesidad educativa especial a lo largo de la vida, aprender un contenido específico escolar es parecido a cualquier hito de desarrollo como comer, el control de esfínter, hablar, caminar, etc. Por lo tanto, así como implementar metodologías para que tu hijo deje el pañal o comience a caminar, debes tener <b>herramientas</b> frente algún aprendizaje académico que no se ha logrado adquirir. Es aquí donde finalmente, es necesario para ejecutar el proceso de intervención por parte del especialista en conjunto con la familia, enfocarse en las metodologías, ya que, dependerán del perfil de cada niño, sus habilidades, capacidades, necesidades o estilo de aprendizaje. Existen un sinfín de actividades lúdicas, uso de material didáctico o metodologías que pueden ayudar a desarrollar o potenciar algún aprendizaje. En <b>Cognitivo</b> contamos con profesionales idóneos para poder ayudar a tu hijo y a ti con este tipo de problemáticas.</p>
                    <p><b>Como padres o cuidadores deben mirar a sus hijos, confiar en ellos, pero ser conscientes de sus posibilidades y limitaciones para generar experiencias que sean gratificantes para ellos y que les permitan aprender.</b></p>
                    <p>Si quieres saber más acerca de cómo estimular y apoyar a tu hijo, no te puedes perder el próximo <b>curso Teórico-Práctico</b> de nuestro centro de terapias Cognitivo ¿Cómo ayudo a mi hijo a desarrollar habilidades y aprendizajes escolares? el día 14 de julio a las 10 hrs.</p>
