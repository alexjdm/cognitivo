                    <br>
                    <p>La mayoría de los padres que llegan a Cognitivo están preocupados porque <b>sus hijos no hablan</b>. En base a esto, lo que más les intriga es saber cuándo lo harán o <b>cómo poder ayudarlos en este proceso</b>. Lo que muchos padres NO saben es que <b>estos pequeños se comunican antes de decir sus primeras palabras.</b></p>
                    <p>Existen diferentes formas de comunicarnos, de hecho, los niños deben adquirir diferentes <b>aptitudes</b> antes de pronunciar sus primeras palabras. Esto se refiere a que, si bien, <b>queremos que los niños obtengan lenguaje oral</b>, hay cosas previas a esto que permiten un intercambio social, por ejemplo: aptitudes visuales (observación mutua, seguimiento visual, observación referencial), aptitudes auditivas (localización del sonido, atención aditiva), aptitudes motrices (imitación motriz/modelado, imitación motriz corporal y gestual, combinación de movimientos motores y sonidos), aptitudes preorales (imitación oral/verbal, imitación de los sonidos del habla), aptitudes pragmáticas tempranas (habilidades de alternancia, signos de comunicación social), aptitudes cognitivas (la permanencia del objeto, causa y efecto, medios y fines).</p>
                    <p>¿Qué hacemos entonces? ¿Cómo nos comunicamos con ellos?</p>
                    <p>Algunos padres comentan que no hablan mucho con sus hijos ya que ellos no hablan. Esto es un error, ya que, <b>el lenguaje se adquiere por imitación</b>, entonces debemos hablar con ellos constantemente.</p>
                    <p>Una de las cosas que los niños deben hacer es <b>disminuir el uso de celulares y tablet</b>, ya que, éstas no generan un diálogo. Ningún aparato electrónico reemplaza la estimulación de los padres.</p>
                    <p>Se deben entregar <b>instrucciones claras y simples</b>, estimulando el <b>contacto visual</b> con su hijo, <b>categorizaciones, asociaciones, juegos y cantar juntos</b>. Puedes ayudarlo con los sonidos de los animales y medios de transporte, etc. En actividades cotidianas como el bañarse puedes nombrar las partes del cuerpo, cuando lo vistas nombra la ropa o los colores de esta, cuando les des la comida comenta lo que come y nombra los alimentos, etc.</p>
                    <p><b>Si el niño no emite palabras y sólo apunta</b> es conveniente que la familia se ponga de acuerdo para simular que no le entendemos cuando él apunta lo que quiere. De esta forma el niño intentará al menos nombrarlo (independiente de cómo lo diga). En este caso es conveniente tomar el objeto y decirle “¿Quieres este auto?, toma, esto se llama auto”, se lo facilitamos para luego preguntar “¿Cómo se llama esto?”.</p>
                    <p><b>Una de las formas más efectivas para comunicarse</b> con su hijo/a es a través del juego, ya que la actividad de <b>jugar es irremplazable</b> para el desarrollo psicosocial del niño. Le permite interactuar con los demás, <b>crear vínculos</b>, iniciarse en las normas, <b>aprender a compartir, respeto turnos</b> y todo ello resulta clave para el desarrollo personal y social. Cuando el niño juega con otros gesta futuras habilidades sociales y comunicativas, a la vez, desarrolla sus capacidades lingüísticas. La gran mayoría de <b>actividades del juego desarrollan las funciones del lenguaje y las formas de expresión</b>. La estimulación del lenguaje a través del juego es de suma importancia y está estrechamente relacionado al desarrollo cognitivo y social. <b>“Todos los aprendizajes más importantes de la vida se hacen jugando”</b>. La mejor forma de comunicarse con su hijo y ayudarlo a desarrollar lenguaje es jugando con él.</p>
                    <p>¿Cómo identificó si existe un retraso del lenguaje?</p>
                    <p>Un niño presenta <b>retraso en el lenguaje cuando</b>: desde el punto de vista cuantitativo (cantidad de vocabulario) o cualitativo (diferentes áreas del lenguaje) está por debajo de la cifra media de los niños de su edad. El retraso en el lenguaje se produce cuando el niño llega a los 18 meses de edad y no dice ninguna palabra, o cuando a los 2 años de edad no consigue unir dos palabras para formar frases sencillas, o <b>no muestra intención de comunicarse</b> con los demás ya sea verbalmente o mediante gestos. Si ves en tu hijo alguno de estos indicadores, es recomendable consultar con un Fonoaudiólogo.</p>
                    <p>Si quieres saber más acerca de cómo estimular a tu hijo, no te puedes perder el próximo <b>curso</b> de nuestro centro de terapias Cognitivo <b>"¿Cómo me comunico con mi hijo si no habla?"</b> el día 9 de junio a las 10 hrs.</p>
