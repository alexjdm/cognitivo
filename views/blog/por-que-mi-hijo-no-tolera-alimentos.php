                </div>
                <div class="entry-content">
                    <br>
                    <p>Los problemas de alimentación son cada vez más comunes, sobretodo si los asociamos a la condición del Espectro Autista.</p>
                    <p>Tenemos muchos niños que presentan rechazo a ciertos alimentos, selectividad excesiva con colores y la mezcla de éstos, temperaturas o sabores determinados; llegando incluso, al extremo de mirar un alimento y vomitar o escapar por toda la casa, con tal de que ese alimento no llegue a ser ingerido.</p>
                    <p>Esta situación provoca mucha angustia, ya que en algunas ocasiones, hay niños que solo logran ingerir un solo alimento y de una manera específica. Por esta situación, es que hay padres desesperados al no saber si debemos obligar al niño a comer, si  es algo asociado al diagnóstico, si es una pataleta o si debemos conformarnos con darle solo el alimento que acepta.</p>
                    <p>Muchos especialistas del área de la salud no saben cómo abordar este tema. Incluso nutricionistas recomiendan buscar alternativas de suplementos alimenticios con el fin de prevenir la desnutrición, pero la problemática de base no está siendo abordada de la manera en que nuestros niños lo necesitan.</p>
                    <p>Para entender más de ésta problemática, debemos comprender que pueden existir varias causas que la estén generando. Una de éstas, puede radicar en el funcionamiento del procesamiento sensorial. Cuando logramos identificar los perfiles sensoriales de nuestros niños (véase en el artículo anterior “¿Qué significa que mi hijo tenga dificultades en el procesamiento sensorial?”), podemos saber si tiene aversión a ciertas texturas o si no logra discriminar las características del bolo alimenticio, ya que la boca, al ser una de las áreas corporales con más receptores sensoriales, se torna mucho más sensible a los estímulos y con mayor intensidad en un procesamiento sensorial exacerbado.</p>
                    <p>Una vez que podamos identificar cuál es el motivo y qué perfil sensorial presenta el niño, entonces podemos crear un plan de trabajo para poder abordar la alimentación como un hito del desarrollo, como una necesidad básica, pero también como una instancia de disfrute y participación social con los miembros de la familia que puedan involucrarse.</p>
                    <p>Existen técnicas de abordaje para cada necesidad. Podremos revisar la mayoría de éstas en el próximo taller para padres “Integración Sensorial y Alimentación” a realizarse el 12 de Mayo en la sucursal de La Florida en el Centro de Terapias Cognitivo.</p>
                    <p>Los esperamos.</p>
