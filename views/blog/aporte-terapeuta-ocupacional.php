                    <br>
                    <p>La terapia ocupacional es una disciplina que abarca áreas desde una visión holística hacia sus usuarios. En el caso del área infanto-juvenil, trabaja desde la estimulación temprana, hasta la habilitación y rehabilitación de usuarios en áreas del desempeño tales como las actividades de la vida diaria básicas (higiene, alimentación, vestuario, etc.) e instrumentales (manejo de dinero, hablar por teléfono, hacer compras, etc.), educación y por sobretodo el JUEGO.</p>
                    <p>Considerando que el juego, es la ocupación más significativa de un niño o adolescente, se utiliza como herramienta para lograr objetivos terapéuticos, con el fin de que los avances sean relevantes para el quehacer de los niños, por lo que tu hijo SIEMPRE trabajará a través del JUEGO.</p>
                    <p>Por otro lado, la disciplina abarca aspectos de relevancia para el desarrollo de tus hijos, tales como la motricidad gruesa y fina, habilidades cognitivas básicas y superiores, el desarrollo socioemocional y la integración sensorial.</p>
                    <p>Tu hijo puede ser derivado a terapia ocupacional, por cualquier especialista de la salud o educación, ya sean neurólogos, psiquiatras, pediatra, fonoaudiólogos, psicólogos, psicopedagogos, educadores, entre otros.</p>
                    <p>Se trabaja con diagnósticos de <b>carácter permanente</b> (Trastorno del espectro autista, déficit intelectual, síndromes genéticos, trastorno de déficit atencional, etc.) y de <b>carácter transitorio</b> (trastornos de aprendizaje, trastornos sensoriointegrativos, retraso en el desarrollo psicomotor, etc.)</p>
                    <p>En Cognitivo contamos con profesionales de esta disciplina, certificados en integración sensorial, especialistas en alimentación y neuropsicología infantil.</p>
