                    <br>
                    <p>La fiesta de año nuevo es una instancia para compartir en familia y llenarnos de la emoción de comenzar un nuevo año junto a los que amamos. Sin embargo, muchas veces, al llegar las doce de la noche, la fiesta se transforma en un mal momento debido a la descompensación de nuestros niños con la pirotecnia.</p>
                    <p>Hoy en Cognitivo, queremos ayudarte con algunos consejos para que la noche de año nuevo no se transforme en una pesadilla.</p>
                    <p>En primer lugar, debes conocer el perfil sensorial de tu hijo. Su terapeuta ocupacional ya les habrá mencionado las palabras hiperreactividad auditiva; si es el caso de tu hijo, entonces estarás al tanto de que su procesamiento auditivo es más exacerbado que el nuestro, por lo que cualquier estímulo auditivo fuerte y sorpresivo provocará evidente malestar, llantos, huídas, desorientación e incluso golpes. Por lo tanto, es de suma importancia que NO lo expongas a la pirotecnia, por el contrario, será de gran utilidad encontrar un lugar seguro, conocido para él o ella, en donde el nivel de ruido ambiente se pueda reducir al mínimo y si puedes acompañarlo con alguna actividad de disfrute, mucho mejor. El uso de tapones o protectores auditivos de la construcción , también son una buena opción para aislar el sonido.</p>
                    <p>En el caso de que tu hijo no presente el perfil sensorial antes mencionado, es importante entender que la pirotecnia de por sí es un estímulo fuerte y sorpresivo, por lo que de igual manera deberás tomar precauciones, como anticipar que a cierta hora (mostrar reloj y explicar gráficamente)  comenzará el show pirotécnico (podemos mostrar videos anteriormente) y el significado de éstos para darle un sentido social. También, es de suma relevancia acompañar este proceso con abrazos,  masajes, juegos motores previos al show, todo lo que estimule el sistema propioceptivo (noción del propio cuerpo a través de la contracción muscular y tacto profundo).</p>
                    <p>En el caso de descompensación debes seguir los siguientes pasos: sacar a tu hijo del estímulo estresor y llevarlo a un ambiente silencioso y conocido; abrazar, contener físicamente con el fin de bajar el nivel de alerta a través de la propiocepción; una vez calmado, podemos explicar lo sucedido. NUNCA: obligar a estar si no quiere; cambiar estímulo por otro similar (exposición a videos o canciones infantiles); pedirle que no grite (muchas veces los niños prefiere gritar para tratar de inhibir el ruido externo a través de la autogestión de éstos); gritarle y disgustarse con ellos.</p>
                    <p>Esperamos que estos consejos puedan ser de utilidad para que vivas una linda fiesta de año nuevo. Feliz año 2019, les desea Cognitivo.</p>
