                        <h4>El proceso en dificultades de aprendizaje</h4>
                        <p>El profesional debe tener conocimientos de psicología, pedagogía y estrategias.</p>
                        <p>Lo que debe saber hacer un profesional de esta área es observar, evaluar, analizar y planear. Se debe comenzar conociendo el entorno familiar, escolar y social del niño, ademas de conocer qué y como se le esta enseñando y como el alumno lo esta aprendiendo. Con dichos antecedentes, se realiza la <b>evaluación psicopedagogía</b> en la cual se indaga en su área socio emocional (ámbito familiar, social y escolar) y ademas el área cognitiva (atención - concentración, razonamiento, clasificación, seriación entre otras) e instrumental (lectura, escritura y calculo). Una vez terminada la evaluación, se hacen las revisiones y análisis correspondientes para poder emitir un diagnostico y luego planear la intervención y estrategias que implementara con el niño, la familia y la escuela para ayudarlo a superar las dificultades.</p>
                        <img src="dist/images/blog/problemas-aprendizaje.jpg" alt="Problemas de aprendizaje" class="pull-right" />

                        <h4>Entonces ¿Cuándo acudir a una psicopedagoga/o?</h4>
                        <p>Si el profesor o usted como apoderado detecta que su hijo/a presenta <b>dificultades en</b> algunas áreas como <b>lectura, escritura o cálculo</b>, se ésta quedando atrás en algunos contenidos, tiene dificultades en <b>mantener su atención, manifiesta inquietud excesiva o problemas de conducta</b>, entonces, es aconsejable que recurra a un psicopedagogo para que a través de un buen diagnóstico y tratamiento pueda superar las dificultades.</p>
                        <p>Por lo general, un psicopedagogo trabaja con necesidades de carácter transitorio (Trastornos de aprendizaje, dislexia, disgrafía, discalculia, déficit atencional, rendimiento intelectual “limítrofe”, entre otras), pero <b>en Cognitivo</b> también <b>entregamos atención a necesidades de carácter permanente</b>, ya que consideramos necesario potenciar y desarrollar habilidades y capacidades en todos los niños.</p>

