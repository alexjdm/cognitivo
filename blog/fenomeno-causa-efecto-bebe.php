							<h4>Estimulación para niños entre 1 – 4 meses</h4>
							<ul>
								<li>Permítale tocar juguetes que suenen (<strong>por ejemplo, un muñeco de goma que al presionarlo suavemente emite un sonido</strong>). Si el niño no lo hace espontáneamente, coloque sus manos encima de las de él y presiónelas hasta producir el sonido. Si el niño se interesa, hágalo varias veces hasta que intente hacerlo por sí mismo.</li>
								<li>Cuando el niño esté tomando leche, retírelo suavemente del pezón o mamadera y después de una pausa de descanso, vuélvalo a colocar. El niño irá asociando la obtención de la leche mediante su propio succionar en el pezón o mamadera.</li>
								<li>En frascos plásticos transparentes coloque pepas, botones, piedritas, etc. y frente al niño mueva el frasco; así podrá conocer la causa del sonido.</li>
								<li>Colóquese frente al niño, muéstrele una mano y en cuanto él la mire, desplácese lentamente hacia un lado. Entonces haga sonar los dedos (<strong>mediante chasquidos</strong>), después desplace hasta el centro la mano y una vez allí colóquela junto a su otra mano y comiéncela a desplazar hacia el otro lado. Repita allí la acción con los dedos. Cuando el niño haya visto la causa del sonido y reaccione, haga sonar la mano que dejó en el centro para que el niño solito vuelva la cabeza y encuentre su mano; cuando lo haga, lleve de nuevo la mano hacia el lado como lo hizo al inicio. Ahora usted está con sus 2 manos, una a cada lado de la cabeza del bebé (<strong>sin salirse del campo visual de él</strong>). Chasquee los dedos alternando las manos y dándole tiempo al niño de encontrar la que suena. Béselo al finalizar el juego.</li>
								<li>Muévase a diferentes distancias, hablándole al niño para que él la siga con la mirada.</li>
								<li>Tome ambas manos del niño y llévelas una hacia la otra, palmeándolas.</li>
							</ul>
							
							<h4>Estimulación para niños entre 4 – 8 meses</h4>
							<ul>
								<li>Coloque al niño boca arriba, y al alcance de sus piernas móviles, de tal modo que al mover aquellas toque los móviles. Prémielo aplaudiendo y permita que repita la acción.</li>
								<li>Acerque al niño un radio transistor y enséñele como dar vuelta al botón que lo prenda y cómo se apaga. El niño repetirá su acción para provocar el sonido.</li>
								<li>Cuando el niño emita un gorjeo o balbuceo, imítelo, estableciendo una conversación con él.</li>
								<li>Coloque música suave y espere a que el niño preste atención; entonces baje el volumen de la música y tararee usted permitiendo que el lactante le observe, después, anímelo a él para que “cante”. Repita la secuencia varias veces.</li>
								<li>Cuando mude al niño después de bañarlo, háblele sobre lo bien que se siente estando seco; cuando lo alimente, háblele después lo satisfecho que se siente al calmar el hambre; cuando lo vista hágale notar verbalmente la diferencia que hay entre ponerse ropa y no haberlo hecho, no importa que el niño no comprenda plenamente el sentido de sus palabras; es conveniente que usted use inflexiones en la voz.</li>
							</ul>
							
