                    <br>
                    <p>Recibir el diagnóstico de Trastorno del Espectro Autista (TEA) en tu hijo, no es fácil. Generalmente, cuando nos dan esta noticia, no logramos entender a cabalidad de qué se trata, cuáles son los niveles de funcionamiento, las diferentes terminologías e incluso, distintos nombres para el mismo diagnóstico. Empezamos a recorrer un camino lleno de profesionales y especialistas que nos comienzan a dar indicaciones que muchas veces no logramos entender. Cada día se nos presenta una duda nueva y sentimos que el camino va cuesta arriba.</p>
                    <p>En este momento, es fundamental el poder tener claridad y no informarse a través de medios informales como el internet, en donde abundan conceptos erróneos y muchas veces, negligentes con respecto al proceso de diagnóstico/intervención.</p>
                    <p>Por otro lado, el integrarse a grupos de padres que estén viviendo la misma situación, nos puede ayudar muchísimo, sobre todo para poder expresar nuestras emociones, sentir apoyo en la identificación del proceso en otros y adquirir nuevas experiencias que puedan fortalecer este nuevo mundo al que ingresamos. Sin embargo, es importante entender, que no todos nuestros hijos son iguales a pesar de presentar el mismos diagnóstico, por lo que, lo que resultó en un niño no nos garantiza el éxito con otro. Lo único que podrá garantizar el éxito, es aprender a conocer a nuestro hijo y entender que, independiente de un diagnóstico o condición, todos tenemos fortalezas y debilidades y la clave para comenzar a trabajar, es poder identificarlas.</p>
                    <p>Por lo tanto, las profesionales especializadas en el diagnóstico, serán las encargadas de guiarte en este nuevo camino, entregándote herramientas para conocer la condición del espectro autista, pero también las fortalezas y desafíos de tu hijo para así, comenzar a construir la base de lo que será un largo camino, que sin duda, con amor, paciencia y comprensión, podrás aprender a vivir.</p>
                    <p>En este curso inicial, hablaremos de las bases del TEA, el proceso diagnóstico, las clasificaciones, los tipos de intervenciones y consejos de utilidad para comenzar a organizarte y acompañarte en este camino lleno de cosas nuevas.</p>
                    <p>Permite que Cognitivo te acompañe y ven a participar con nosotros en este taller para padres a realizarse el sábado 4 de Mayo a las 15 horas en nuestra sucursal de La Florida.</p>
