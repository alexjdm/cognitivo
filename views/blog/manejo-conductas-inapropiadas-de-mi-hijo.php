                    <div class="entry-content">
                        <br>
                        <p>Dentro de la población infantil son comunes las expresiones de enfado, descontrol o desregulación emocional por medio de pataletas o comportamientos disruptivos. Estas pueden ocurrir por diversos factores, ya sean personales o ambientales, y es más común observarlas en niños que presentan condición del Espectro Autista, debido a las dificultades que presentan para manifestar sus estados emocionales, sus necesidades fisiológicas, sus intereses o deseos, entre otros aspectos.</p>
                        <p>Las conductas inapropiadas interfieren o limitan los aprendizajes y la vida cotidiana de los niños, sus familias y los entornos cercanos, por ello es importante buscar las estrategias que permitan resolverlas.</p>
                        <p>Debido a que las causas que las gatillan son múltiples, frecuentemente son difíciles de abordar, además, generan sentimientos de angustia tanto en los padres, como en los profesores, pero fundamentalmente en los niños que las manifiestan. Dentro de estas causas encontramos las dificultades de la comunicación, de la conducta, de la regulación emocional y del funcionamiento del procesamiento sensorial. La primera de ellas se relaciona con la manera en que los niños comprenden lo que sucede en el medio externo y expresan su mundo interno, la segunda con la forma de conseguir o evitar algún objeto o situación, la tercera con las herramientas que poseen para controlar sus emociones, y la última se relaciona con la manera de recibir y responder a la información del ambiente y de su propio cuerpo. Dependiendo de cada causa, el abordaje será distinto.</p>
                        <p>Una vez que se identifican las causas y funciones de las conductas, será posible guiar una intervención adecuada para cada caso y las estrategias pertinentes para la resolución del conflicto. Durante el próximo taller podremos revisar varias ideas en torno a las conductas disruptivas o inapropiadas; entre ellas qué son, cuáles son sus causas o factores que las desencadenan, algunos métodos de evaluación de la conducta, sus funciones y las estrategias para abordarlas. Este encuentro se realizará el día sábado 1 de Diciembre en la sucursal de La Florida de nuestro Centro de Terapias Cognitivo.</p>
                        <p>Los esperamos.</p>
