                    <h1 class="entry-title">¿Por qué mi hijo juega en las sesiones de terapia?</h1>
                    <div class="entry-meta">
                        <span class="date"><i class="fa fa-calendar"></i>Enero 22, 2018</span>
                        <span class="author"><i class="fa fa-user"></i>Por Equipo Cognitivo</span>
                    </div>
                    <div class="post-thumb">
                        <img src="dist/images/blog/juego-terapias.jpg" alt="Juego en terapias" class="img-responsive" />
                    </div>
                    <div class="entry-content">
                        <p>Esta es una pregunta muy común entre padres y cuidadores de niños y niñas que asisten a terapias de psicología. Ante esto, deben saber que <b>es normal cuestionarlo y preguntarse por qué los niños van a jugar en terapia</b>, ya que estamos acostumbrados a ver procesos terapéuticos relacionados por sobre todo a la conversación.</p>
                        <p>La conversación y el lenguaje verbal es la forma ideal de comunicación entre los adultos, pero en las niñas y niños no es igual. Los niños y niñas <b>en las primeras etapas no poseen tanta facilidad para conversar como los adultos</b> y mucho menos desplegar conversaciones profundas con alguna persona que no es cercana al núcleo familiar. En ese sentido ¿cómo podemos comunicarnos con los niños en el ambiente terapéutico?</p>
                        <p><b>El juego es por excelencia el lenguaje de las niñas y los niños</b>, es una actividad común y cercana que, quita la ansiedad en los espacios terapéuticos y que facilita la comunicación con los terapeutas y las intervenciones para incorporar los diversos cambios y potenciar las habilidades propias de un tratamiento psicológico. Pero no es sólo una herramienta que facilita la comunicación, sino que el juego nos informa acerca de importantes procesos propios del desarrollo de los niños y niñas tales como biológicos, neurológicos, capacidad de aprendizaje y la forma de vinculación con el ambiente y las personas.</p>
                        <img src="dist/images/blog/juego-terapias-niños.jpg" alt="El juego, el lenguaje de los niños" class="pull-right" />
                        <p>Entonces, es el juego un elemento fundamental para procesos de evaluación y también para procesos terapéuticos. Esto porque <b>los niños y niñas en terapia</b> y por medio del juego <b>pueden expresar sus deseos, sus rabias y sus intereses, así como todo tipo de emociones</b>. También es un espacio para fomentar la creatividad, potenciar la motricidad, tolerancia a la frustración, además de incorporar el descubrimiento de diversos elementos internos y externos en un espacio lúdico.</p>
                        <p>Finalmente, destacamos el juego como un elemento que es idóneo para el trabajo con niños y niñas, es entretenido y cercano, es algo común para ellos lo que les da libertad para desenvolverse de manera suelta y sin tanta ansiedad, para poder transmitir sus necesidades lo que <b>otorga un valioso momento para conocer de mejor manera a los niños y niñas y con ello la oportunidad de intervenir con estrategias</b> que siguen la lógica del juego otorgando un espacio que , si bien es de trabajo, resulta cercano y entretenido para ellos.</p>
