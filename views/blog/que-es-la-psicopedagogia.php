                        <p>La psicopedagogía es la rama de la psicología que estudia y se ocupa de los <b>problemas de aprendizaje</b>. Desde la psicopedagogía el aprendizaje se ve como un proceso en el que intervienen varios factores como la atención, su orientación y mantenimiento, la memoria a corto plazo, procesos donde la información pasa a largo plazo, procesos de recuperación de información, procesos evolutivos y el entorno.</p>
                        <p>Existe el prejuicio que los usuarios de la psicopedagogia son solo niños en etapa escolar con problemas de aprendizaje, pero también se pueden realizar trabajos de prevención en preescolares, <b>prevención en etapa escolar para evitar de fracaso</b>, orientación en nuevas técnicas de aprendizaje a profesionales de la enseñanza como apoyar en el desarrollo personal y social de los niños y adolescentes, ayudar en la orientación laborar, elaborar programas de atención a la diversidad, orientar a las familias en los centros educativos con, por ejemplo, escuelas de padres, y por supuesto, elaborar programas de apoyo al aprendizaje. Siempre nos encontramos en proceso de aprendizaje, por lo que en cualquier etapa de la vida se puede presentar alguna necesidad frente a este.</p>
