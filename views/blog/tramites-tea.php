                    <div class="entry-content">
                        <p>Es común que los servicios públicos no se caractericen por su fluidez y rapidez en la atención. Esto lo vuelve un problema cuando el trámite que debemos realizar es con nuestro hijo(a) con TEA, generalmente porque ellos tienen problemas con las esperas. La mayoría de la gente que nos atiende no conoce casi nada del trastorno, por lo que, sus respuestas no son las más adecuadas, e incluso muchas veces, las más educadas. Y decimos generalmente porque tenemos casos donde el trato es mejor de lo que esperamos (<a href="http://www.emol.com/noticias/Nacional/2017/10/20/880084/Funcionaria-de-Metro-que-ayudo-a-calmar-a-nino-con-autismo-No-habia-dimensionado-que-habia-causado-tal-impacto-en-las-personas.html">Funcionaria Metro que ayudó a calmar niño con TEA</a>)</p>
                        <img src="dist/images/blog/frustracion-nino-tea.jpg" alt="Frustración niño con su madre" class="pull-right" />
                        <p>Los padres de nuestros niños nos han contado varias veces de sus intentos de obtener la cédula de identidad en el Registro Civil (o como mejor conocemos “sacar carnet”), pero varias historias terminan con un niño descompensado intentando realizar el trámite. Esto a pesar de indicar a los funcionarios que es un niño con TEA.</p>
                        <p>Por lo mismo, escribimos este artículo para intentar difundir un poco más este servicio que presta el Registro Civil y que es de mucha utilidad para realizar trámites a personas con dificultades de asistir a la oficina.</p>

                        <h4>¿Sabías que el Registro Civil realiza atenciones en terreno?</h4>

                        <p>Este servicio ayuda a quienes no pueden concurrir a las oficinas del Registro Civil y les facilita el acceso a realizar trámites como cédulas de identidad y certificados.</p>
                        <p>Los productos y servicios disponibles en terreno son:</p>
                        <ul>
                            <li>Cédula de identidad para chilenos y extranjeros.</li>
                            <li>Certificados de nacimiento, matrimonio, defunción, posesiones efectivas, anotaciones vigentes de vehículos motorizados, de antecedentes, de discapacidad, hoja de vida del conductor, y de multas de tránsito no pagadas.</li>
                            <li>Orientación y respuesta a consultas.</li>
                            <li>Posibilidad de realizar reclamos, sugerencias y/o felicitaciones.</li>
                        </ul>
                        <h4>¿Cómo solicitar atención en terreno?</h4>
                        <p>Ingresa a este sitio <a href="https://registrocivilwp.srcei.cl/tramites/solicitud-de-atencion-en-terreno/">Solicitud de atención en terreno</a> y encontrarás toda la información oficial sobre el asunto.</p>
                        <p>Básicamente se trata de descargar un formulario, el cual debemos llenar y enviar por correo electrónico. Algunos de los requisitos es que la persona que lo solicite indique su nombre, RUN, productos o servicios a solicitar y nacionalidad.</p>
                        <p>La atención en terreno no tiene costo adicional.</p>
