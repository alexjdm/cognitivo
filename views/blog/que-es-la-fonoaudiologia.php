                        <h4>Entonces ¿Cuándo acudir a un(a) fonoaudiologo/a?</h4>
                        <img src="dist/images/blog/problemas-audicion.jpg" alt="Problemas de audición" class="pull-right" />
                        <p>Puedes ser derivado al fonoaudiólogo por cualquier especialista de la salud y/o educación, sin embargo, si tu hijo presenta alguna de estas características: <b>Retraso del habla, dificultades para pronunciar algunas consonantes o palabras, dificultad en poder expresarse o comprender el lenguaje, tartamudez, problemas en la audición, disfonías</b>, entre otras, <b>es recomendable asistir a una evaluación fonoaudiológica</b>.</p>
                        <p>El poder asistir al fonoaudiólogo ayudará a poder detectar, diagnosticar y tratar de manera temprana cualquier desorden en la comunicación que se presente en tus seres cercanos.</p>
